import Foundation

extension UIColor {
  
  public class func gitHubGreenColor() -> UIColor {
    return UIColor(red: 0.420, green: 0.780, blue: 0.270, alpha: 1.0)
  }
  
  public class func gitHubRedColor() -> UIColor {
    return UIColor(red: 0.740, green: 0.170, blue: 0.0, alpha: 1.0)
  }
}